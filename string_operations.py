# concatenation
namaDepan = "Messal"
namaBelakang = "Veronica"

print(namaDepan + " " + namaBelakang)

# Repetition
print("ha" * 10)
print("ha \n" * 10)

# slicing
x = "hello"
print(x[1:90])

# length
print(len(x))

#uppercase dan lowercase
print(x.upper())
print(x.lower())

# replace
name = "ferendela"
print(name.replace("e", "a"))